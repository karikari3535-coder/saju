/** Claude messages API용 payload 조립 (모델·max_tokens 포함) */
export function buildClaudePayload(block: DataBlock, model: string) {
  return {
    model,
    max_tokens: 3300,
    temperature: 0.85,
    system: SYSTEM_PROMPT,
    messages: [
      {
        role: 'user' as const,
        content: buildUserMessage(block),
      },
    ],
  }
}
